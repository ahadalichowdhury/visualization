// generateOutputs generates output values
func (g *TerraformGenerator) generateOutputs() string {
	var output strings.Builder

	output.WriteString("# Outputs\n")
	for _, node := range g.nodes {
		resourceName := sanitizeName(node.Data["label"].(string))
		switch node.Type {
		case "load_balancer":
			output.WriteString(fmt.Sprintf(`output "%s_dns" {
  value = aws_lb.%s.dns_name
}
`, resourceName, resourceName))
		case "api_gateway":
			output.WriteString(fmt.Sprintf(`output "%s_url" {
  value = aws_api_gateway_rest_api.%s.execution_arn
}
`, resourceName, resourceName))
		}
	}

	return output.String()
}

// Helper functions
func sanitizeName(name string) string {
	// Convert to lowercase and replace spaces with underscores
	name = strings.ToLower(name)
	name = strings.ReplaceAll(name, " ", "_")
	name = strings.ReplaceAll(name, "-", "_")
	return name
}

func getStringOrDefault(config map[string]interface{}, key, defaultValue string) string {
	if val, ok := config[key].(string); ok {
		return val
	}
	return defaultValue
}

func getIntOrDefault(config map[string]interface{}, key string, defaultValue int) int {
	if val, ok := config[key].(float64); ok {
		return int(val)
	}
	return defaultValue
}
