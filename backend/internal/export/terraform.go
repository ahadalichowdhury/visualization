// Generate Lambda Function
func (g *TerraformGenerator) generateLambda(id, name string, config map[string]interface{}) string {
	runtime := getStringOrDefault(config, "runtime", "nodejs18.x")
	memoryMB := getIntOrDefault(config, "memoryMB", 128)

	return fmt.Sprintf(`resource "aws_lambda_function" "%s" {
  function_name = "${var.project_name}-%s"
  role          = aws_iam_role.%s_role.arn
  handler       = "index.handler"
  runtime       = "%s"
  memory_size   = %d
  timeout       = 30

  filename         = "lambda_function_payload.zip"
  source_code_hash = filebase64sha256("lambda_function_payload.zip")

  tags = {
    Name        = "${var.project_name}-%s"
    Environment = var.environment
    ManagedBy   = "Terraform"
  }
}

resource "aws_iam_role" "%s_role" {
  name = "${var.project_name}-%s-role"

  assume_role_policy = jsonencode({
    Version = "2012-10-17"
    Statement = [{
      Action = "sts:AssumeRole"
      Effect = "Allow"
      Principal = {
        Service = "lambda.amazonaws.com"
      }
    }]
  })
}

`, name, name, name, runtime, memoryMB, name, name, name)
}

