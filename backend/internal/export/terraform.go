package export

import (
	"fmt"
	"strings"
)

// NodeInput represents a node from the frontend
type NodeInput struct {
	ID       string                 `json:"id"`
	Type     string                 `json:"type"`
	Data     map[string]interface{} `json:"data"`
	Position map[string]float64     `json:"position"`
}

// EdgeInput represents an edge from the frontend
type EdgeInput struct {
	ID     string `json:"id"`
	Source string `json:"source"`
	Target string `json:"target"`
}

// TerraformGenerator generates Terraform HCL code
type TerraformGenerator struct {
	nodes []NodeInput
	edges []EdgeInput
}

// NewTerraformGenerator creates a new Terraform generator
func NewTerraformGenerator(nodes []NodeInput, edges []EdgeInput) *TerraformGenerator {
	return &TerraformGenerator{
		nodes: nodes,
		edges: edges,
	}
}

// Generate generates the complete Terraform configuration
func (g *TerraformGenerator) Generate() string {
	var output strings.Builder

	// Header
	output.WriteString("# Generated by Architecture Visualization Platform\n")
	output.WriteString("# Date: " + fmt.Sprintf("%v", "now") + "\n\n")

	// Provider configuration
	output.WriteString(g.generateProvider())
	output.WriteString("\n")

	// Variables
	output.WriteString(g.generateVariables())
	output.WriteString("\n")

	// Resources
	for _, node := range g.nodes {
		resource := g.nodeToTerraformResource(node)
		if resource != "" {
			output.WriteString(resource)
			output.WriteString("\n")
		}
	}

	// Outputs
	output.WriteString(g.generateOutputs())

	return output.String()
}

// generateProvider generates provider configuration
func (g *TerraformGenerator) generateProvider() string {
	return `terraform {
  required_version = ">= 1.0"
  required_providers {
    aws = {
      source  = "hashicorp/aws"
      version = "~> 5.0"
    }
  }
}

provider "aws" {
  region = var.aws_region
}
`
}

// generateVariables generates variable definitions
func (g *TerraformGenerator) generateVariables() string {
	return `variable "aws_region" {
  description = "AWS region for resources"
  type        = string
  default     = "us-east-1"
}

variable "environment" {
  description = "Environment name (dev, staging, prod)"
  type        = string
  default     = "dev"
}

variable "project_name" {
  description = "Project name for resource naming"
  type        = string
  default     = "arch-viz"
}
`
}

// nodeToTerraformResource converts a node to Terraform resource
func (g *TerraformGenerator) nodeToTerraformResource(node NodeInput) string {
	config := node.Data["config"].(map[string]interface{})
	label := node.Data["label"].(string)
	resourceName := sanitizeName(label)

	switch node.Type {
	case "api_server", "web_server", "microservice", "worker":
		return g.generateEC2Instance(node.ID, resourceName, config)

	case "database_sql":
		return g.generateRDSInstance(node.ID, resourceName, config)

	case "database_nosql":
		return g.generateDynamoDB(node.ID, resourceName, config)

	case "cache_redis":
		return g.generateElastiCache(node.ID, resourceName, config)

	case "load_balancer":
		return g.generateALB(node.ID, resourceName, config)

	case "queue":
		return g.generateSQS(node.ID, resourceName, config)

	case "object_storage":
		return g.generateS3Bucket(node.ID, resourceName, config)

	case "lambda_function":
		return g.generateLambda(node.ID, resourceName, config)

	case "api_gateway":
		return g.generateAPIGateway(node.ID, resourceName, config)

	case "vpc", "subnet":
		return g.generateVPC(node.ID, resourceName, config)

	default:
		return fmt.Sprintf("# Unsupported node type: %s (%s)\n", node.Type, label)
	}
}

// Generate EC2 Instance
func (g *TerraformGenerator) generateEC2Instance(id, name string, config map[string]interface{}) string {
	instanceType := getStringOrDefault(config, "instanceType", "t3.micro")

	return fmt.Sprintf(`resource "aws_instance" "%s" {
  ami           = data.aws_ami.ubuntu.id
  instance_type = "%s"

  tags = {
    Name        = "${var.project_name}-%s"
    Environment = var.environment
    ManagedBy   = "Terraform"
  }
}

`, name, instanceType, name)
}

// Generate RDS Instance
func (g *TerraformGenerator) generateRDSInstance(id, name string, config map[string]interface{}) string {
	engine := getStringOrDefault(config, "databaseEngine", "postgres")
	instanceType := getStringOrDefault(config, "instanceType", "db.t3.micro")
	storageGB := getIntOrDefault(config, "storage_size_gb", 20)

	return fmt.Sprintf(`resource "aws_db_instance" "%s" {
  identifier           = "${var.project_name}-%s"
  engine               = "%s"
  engine_version       = "15.3"
  instance_class       = "%s"
  allocated_storage    = %d
  storage_type         = "gp3"
  
  db_name  = "mydb"
  username = "admin"
  password = var.db_password  # Store in secrets manager
  
  skip_final_snapshot = true
  
  tags = {
    Name        = "${var.project_name}-%s"
    Environment = var.environment
    ManagedBy   = "Terraform"
  }
}

`, name, name, engine, instanceType, storageGB, name)
}

